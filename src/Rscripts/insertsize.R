#!/usr/bin env Rscript
args <- commandArgs(TRUE)
n<-length(args)
if (n!=3) stop("\nusage: <infile> <sample_name> <outfile>\n")
bitmap(file=args[3],width =14,height =12,type = "png16")
par(ann=F,mar=c(6,5,5,4),xpd=TRUE)
a <- read.delim(args[1],header=FALSE)
name <- paste("Insert size distribution of sample",args[2])
plot(x=a[,1],y=a[,3],type ="l",col ="red",lwd=2)
title(main=name,font.main=2,font=10)
title(xlab="Insert size",ylab="Fraction of pair mapped reads",font.lab=2,font=6)
box()
